function MarkerColors(type_id) {
    switch(type_id) {
        case 1:
            return 'aqua';
        case 2:
            return 'blueviolet';
        case 3:
            return 'darkblue';
        case 4:
            return 'green';
        case 5:
            return 'crimson';
        case 6:
            return 'purple';
        case 7:
            return 'darkgreen';
        case 8:
            return 'chocolate';
        case 9:
            return 'white';
        case 10:
            return 'pink';
        case 11:
            return 'bronze';
        case 12:
            return 'magenta';
        case 13:
            return 'lime';
        case 14:
            return 'teal';
        case 15:
            return 'black';
        default:
            return 'crimson';
}
}

export default MarkerColors;